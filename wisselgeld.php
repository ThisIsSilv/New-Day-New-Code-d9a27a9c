<?php

$geld = intval($argv[1]);


define("GELDEENHEDEN", array(
    50,
    20,
    10,
    5,
    2,
    1

));

foreach (GELDEENHEDEN as $ammount) {
    $aantal = floor($geld / $ammount);
    $geld = $geld - ($aantal * $ammount);
    if ($aantal != 0) {
        echo "$aantal keer $ammount euro" . PHP_EOL;
    }
}













